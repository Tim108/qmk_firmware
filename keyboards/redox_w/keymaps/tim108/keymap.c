layer_state_t layer_state_set_user(layer_state_t state) {
    switch (get_highest_layer(state)) {
        case _PRIMARY:
            set_led_off;
            break;
        case _SECONDARY:
            set_led_green;
            break;
        case _TERTIARY:
            set_led_blue;
            break;
        case _QUATERNARY:
            set_led_red;
            break;
        default:
            break;
    }
  return state;
}


